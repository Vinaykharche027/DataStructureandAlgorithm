// Linear time complexity 
#include<iostream>
using namespace std;
int main(){
    int n;
    cin>>n;
    for(int i=0;i<n;i++){
        cout<<i;
    }
    
}

// Program->using loop to print number->0 to n-1->
// number iteration is directionaly->n
// Time:O(n)

// space->use constant amount of memory for storing integer data
// irrespecitve->
// O(1)